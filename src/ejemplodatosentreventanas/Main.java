    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //Crear un objeto del tipo JDialog que se ha diseñado
        JDialogConfig jDialogConfig = new JDialogConfig(this, true);
        //Cambiar el elemento de la lista que se debe mostrar inicialmente, que
        //  debe ser el que se hubiera seleccionado anteriormente
        jDialogConfig.setPosSelecLista(this.posSelecLista);
        //Mostrar la ventana de diálogo
        jDialogConfig.setVisible(true);
        
        //A partir de aquí se ejecutará cuando se oculte la ventana de diálogo
        
        //Realizar acciones sólo si el usuario ha pulsado el botón Aceptar
        if(jDialogConfig.getOkPressed()) {
            //Guardar el valor que el usuario ha indicado en la lista, para 
            //  que la siguiente vez que se abra la ventana aparezca 
            //  seleccionado el mismo valor
            this.posSelecLista = jDialogConfig.getPosSelecLista();
            //Mostrar en pantalla el valor
            jTextArea1.append("Se ha seleccionado: " + jDialogConfig.getPosSelecLista() + "\n");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

